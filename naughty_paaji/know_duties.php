                        <p align="justify">  The following are the Eleven Fundamental Duties of every citizen of India:</p> <br /> 
                          
<p align="justify">(a) To abide by the Constitution and respect the National Flag and the National Anthem. This means that every citizen of India is required to follow the law as set in the Constitution. He is also supposed to give respect to the national anthem and the national flag by not using them in any disrespectful manner. It is the duty of every Indian citizen to respect the symbols of nation.</p> <br /> 

<p align="justify">(b) To cherish and follow the noble ideals which inspired our national struggle for freedom. Every citizen of India is duty-bound to respect and follow the ideals of patriotism, non-violence and truthfulness that inspired the India&acute;s freedom struggle against the British colonial rule.</p> <br /> 

<p align="justify">(c) To uphold and protect the sovereignty, unity and integrity of India. This duty makes every Indian citizen to live in harmony with the fellow citizens in order to promote the unity and integrity of the country. India is a nation with diverse people. It can only be held together when citizens live in unity with each other.</p> <br /> 

<p align="justify">(d) To defend the country and render national service when called upon to do so. This duty enjoins on the citizens the responsibility to fight for the nation during times of war and external aggression. It is the duty of every citizen of India to serve the country in whatever possible way during a war.</p> <br /> 

<p align="justify">(e) To promote harmony and the spirit of common brotherhood amongst all people of India transcending religious, linguistic and regional or sectional diversities and to renounce practices derogatory to the dignity of women. Every citizen of India should practice religious tolerance and maintain communal harmony in the society. It is also the duty of every citizen to respect women and abstain from offences against the women.</p> <br /> 

<p align="justify">(f) To value and preserve the rich heritage of our composite culture. This duty tells every citizen of India to protect the cultural heritage of the country and not to destroy or distort historical monuments and historical places that are a symbol of India&acute;s glorious past.</p> <br /> 

<p align="justify">(g) To protect and improve the natural environment including forests, lakes, rivers and wild life and to have compassion for living creatures. This duty enjoins on every citizen of India to keep the country clean. Citizens should not throw garbage here and there as it pollutes the environment. Protection of animals and other living creatures is also a responsibility of the citizens.</p> <br /> 
 
<p align="justify">(h) To develop the scientific temper, humanism and the spirit of inquiry and reform. Every citizen of India must develop himself in such a manner that he renders some service to the society either in scientific, humanity or any other field. It is the duty of every citizen to bring about reforms in the system so that the country can progress.</p> <br /> 

<p align="justify">(i) To safeguard public property and to abjure violence. Every citizen of India must not resort to violence under any circumstances and should not destroy public property such as schools, buses, hospitals, trains etc. This causes a disruption of day to day life and affects citizens themselves.</p> <br /> 

<p align="justify">(j) To strive towards excellence in all spheres of individual and collective activity, so that the nation constantly rises to higher levels of Endeavour and achievement.</p> <br /> 

<p align="justify">(k) To provide opportunities for education by the parent or the guardian, to his child, or a ward between the age of 6-14 years as the case may be. It is a duty of every parent or guardian to ensure that their child or ward was provided opportunities for education between the ages of six and fourteen years and to ensure their proper upbringing.</p> <br /> 

<p align="justify">These Fundamental Duties are applicable only to citizens of India and not to the aliens. It is expected that a citizen of India, while enjoying fundamental rights, should also perform these duties. Although there is no provision in the constitution for direct enforcement of any of these duties, yet the courts are guided by these duties while interpreting various laws. No democratic country can ever succeed where the citizens are not willing to be active participants in the process of governance by assuming responsibilities and discharging citizenship duties and coming forward to give their best to the country. 
