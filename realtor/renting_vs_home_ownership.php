                            <td width="71%" class="black9">This is a decision which many people face, and the decision is not as easy to make as it may sound.
                                <br>
                                <br>
                                As a homeowner, you can reasonably expect the equity in your home to increase over time as your mortgage is paid down. That, combined with regular appreciation in property values, can be a rapid and rewarding way to increase your net worth. In contrast, the person renting over the same amount of time is left with no property investment but may have enjoyed lower living expenses and the opportunity to invest in other opportunities.<br>
                                <br>
                                When comparing owning to renting, you have to add up all of the figures, including the cost of your home, the size of your down payment, utilities, immediate repairs, interest rates and insurance, and compare them with how much you are currently spending on rent.<br>
