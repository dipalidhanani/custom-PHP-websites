<p>When working with a REALTOR®, it is important to understand who the 
REALTOR® works for, and to whom is the REALTOR® legally obligated. The 
Canadian Real Estate Association (CREA) requires REALTORS® to disclose 
Agency Relationship to a potential client at the earliest time possible.</p>

<span class="title_red" style="font-size: 16pt;"><strong>Buyers Relationship to REALTORS®</strong></span>

<p>A Buyer has a choice of two relationships with a REALTOR®. As a 
Client, a real estate company acting as a Buyers Agent must do what is 
best for the buyer. A written contract, called a Buyer Agency Agreement,
 establishes buyer agency. It also explains services the company will 
provide, establishes a fee arrangement for the REALTORs® services and 
specifies what obligations a buyer may have. Under such agency, a buyer 
will be obliged to work with that company for a period of time. In 
return, confidence a buyer shares with that company will be kept 
confidential. The REALTOR® is also required to offer professional 
advice, negotiate the best price for the buyer and provide the buyer 
with as much information required to make the right decision.</p>

<p>As a Customer the buyer can expect to be treated fairly and honestly.
 It is important for the buyer to realize that under such a relationship
 the REALTOR® is technically a sub-agent of the seller so that duties 
are owed to that seller. However, the buyer can expect the REALTOR® to 
disclose all pertinent information about a property, not to misrepresent
 any facts, and to honestly answer all questions about the property. 
Under such relationship with the buyer, the REALTOR® must not imply that
 they shall negotiate a price for the buyer as that would be a direct 
conflict with the REALTORs® sub-agency relationship with the seller and a
 violation of our rules and regulations.</p>

<img alt="Sellers Relationship to Realtors®" src="images/sellerstorealtors.jpg" style="margin:0px 6px 6px 0px;" class="content_img" align="left" border="0">

<span class="title_red" style="font-size: 16pt;"><strong>Sellers Relationship to REALTORS®</strong></span>

<p>A real estate company must do what is best for the seller of a 
property. A written contract, called a Listing Agreement, establishes 
sellers agency. It also explains services the company will provide, 
establishes a fee arrangement for the REALTORs® services and specifies 
what obligations a seller must have.</p>

<p>Confidence a seller shares with their REALTOR® must be kept 
confidential from potential buyers and others. That REALTOR® must tell 
the seller anything known about the buyer. For instance, if the REALTOR®
 knows that a buyer is willing to offer more for a property, that 
information must be shared with the seller.</p>

<p>A seller must understand that a REALTOR® working with a buyer as a 
sub-agent is ultimately working with the sellers best interest in mind. A
 REALTOR® working with a buyer, as a Buyer Agent, is working for the 
buyers best interest mind, but may still be compensated by the seller 
through provisions made to the Listing Agent.</p>

<span class="title_red" style="font-size: 16pt;"><strong>Multiple Representation</strong></span>

<p>Occasionally a real estate company will be the agent for both the 
buyer and the seller. The buyer and seller must consent to this 
arrangement in their listing and buyer agency agreements. Under this 
multiple representation arrangement, the company must do what is best 
for both the buyer and seller.</p>

<p>Since the company's loyalty is divided between the buyer and seller 
who have conflicting interest, it is absolutely essential that a 
multiple representation relationship be established in a written agency 
agreement. This agreement specifically describes the rights and duties 
of everyone involved and any limitations to those rights and duties.</p>

